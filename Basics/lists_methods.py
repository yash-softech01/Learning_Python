list1 = [2,4,7]
print(list1)
# add element at the last index
list1.append(9)
print(list1)

# list1 sorting by default ascending order
list1.sort()
print(list1) 

# Sort list1 in descending order
list1.sort(reverse=True)
print(list1)

# New list1

list2 = ['y', 'a', 'm', 'f', 'b']
print(list2)

list2.sort()
print(list2)

list2.reverse()
print(list2)

list2.append('z')
print(list2)

list2.insert(1,'5')
print(list2)